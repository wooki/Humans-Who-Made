	<h1>An index of people who built websites<span>- using humans.txt</span></h1>
	<h2>
		<em>Humans Who Made</em> is an index of who built a specific website, unlike a portfolio or manually updated catalog we
		crawl the web looking for websites that implement the humans.txt standard to determine who was behind each site. This
		has the advantage of ensuring that the people listed on this site really do have access to the site they are claiming to
		have developed and also it allows us to list a huge number of websites and people easily.
	</h2>
