			<h2>Introduction</h2>
			<p><em>Humans Who Made</em> was built in order to give credit to those people who
    actually build the sites that make up the web, or at least give them a way of
    claiming the credit themselves. Rather than having to persuade the website owners
    to devote a portion of their actual website to you it is much simpler and more
    convenient to use a humans.txt file.</p>
			
			<h2>The humans.txt file</h2>
			<p>The file itself is just a simple text file containing whatever content
			about the authors if the site and the site itself that is appropriate.
			Make sure the file is UTF8 encoded to allow for other langauges etc.</p>
			<p>just make sure the file is named humans.txt (all lowercase) and place
			it in the site root. And thats it.</p>
			
			<h2>Suggestions</h2>
			<p>Although the file is free format text the people at humanstxt.org have
			some suggestions about how to format the file.  Essentially they suggest
			a simple and clear set of headers grouping the information into sections
			such as team, thanks and site.  Take a look at their
			<a href="http://humanstxt.org/Standard.php">humans.txt standards</a>
			page for some more ideas.</p>
			
